** WEB APPLICATIONS **
-------------------------------------------------------------
-> A Web Application is a NETWORK COMMUNICATION PROGRAM, which has 2 parts: 1) Client 2) Server

Client sends request to the server, and the 
Server replies/responds to its request.

In Web Dev:
Client = Browser, 
Server = Web Server

This web application is Hosted on the Web server.

The messages which are sent back and forth have to follow certain rules. They are known as "PROTOCOLS".

In case of web applications, we use "HTTP PROTOCOL".

Message sent from browser (client) -> web server (server) is called "HTTP Request".

